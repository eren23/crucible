# ---------------------------------------------------------------------------


def assemble_source(ctx: RenderContext) -> str:
    """Render the full jupytext-percent source."""
    header = [
        "# ---",
        "# jupyter:",
        "#   jupytext:",
        "#     text_representation:",
        "#       extension: .py",
        "#       format_name: percent",
        "#       format_version: '1.3'",
        "#       jupytext_version: 1.16.0",
        "#   kernelspec:",
        "#     display_name: Python 3",
        "#     language: python",
        "#     name: python3",
        "# ---",
        "",
    ]
    parts: list[str] = ["\n".join(header)]
    builders = [
        build_header_cell,
        build_install_cell,
        build_clone_cell,
        build_secrets_cell,
        build_config_cell,
        build_train_cell,
        build_eval_cell,
        build_upload_cell,
    ]
    for builder in builders:
        kind, lines = builder(ctx)
        marker = "# %% [markdown]" if kind == "markdown" else "# %%"
        if kind == "markdown":
            flat: list[str] = []
            for line in lines:
                # Multi-line strings (e.g. spec descriptions) arrive as a single
                # entry with embedded '\n'; split so every physical line gets
                # its own '# ' prefix.
                for sub in line.split("\n"):
                    flat.append(f"# {sub}" if sub else "#")
            cell_body = "\n".join(flat)
        else:
            cell_body = "\n".join(lines)
        parts.append(f"{marker}\n{cell_body}\n")
    return "\n".join(parts)


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _repr(value: str) -> str:
    """Python repr() that always produces a single-line string literal."""
    return repr(value)
